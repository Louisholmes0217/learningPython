# 2d lists are simply lists of lists, and are indexed in the same way

multiList = [
            ["burger", "pizza", "cake"],
            ["blue", "red", "green"],
            ["big", "medium", "small"]
            ]

# printing entire 2d list
print(multiList)

# printing second list in the 2d list
print(multiList[1])

# printing second item from second list in 2d list
print(multiList[1][1])