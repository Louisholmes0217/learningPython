# working with index operator

name = "Awesome Name"

# print first char of name
print(name[0])

# print last char of name
print(name[-1])

# print first 3 chars of name
print(name[0:3])

# print last 3 chars of name
print(name[-3:])

# print everything after the first 4 chars of name
print(name[4:])

